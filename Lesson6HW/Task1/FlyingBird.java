package homeworkl6;

public abstract class FlyingBird extends Bird{
	private double highOfFly, speedOfFly;

	public double getHighOfFly() {
		return highOfFly;
	}

	public void setHighOfFly(double highOfFly) {
		this.highOfFly = highOfFly;
	}

	public double getSpeedOfFly() {
		return speedOfFly;
	}

	public void setSpeedOfFly(double speedOfFly) {
		this.speedOfFly = speedOfFly;
	}
	
}
